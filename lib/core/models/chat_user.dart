class ChatUser {
  final int id;
  final String name;
  final String fullName;
  final String lastMessage;
  final String time;
  final String imagePath;

  ChatUser({
    required this.id,
    required this.name,
    required this.fullName,
    required this.lastMessage,
    required this.time,
    required this.imagePath,
  });
}
